export function createAsciiBannerPlugin(packageJson) {
	return {
		name: 'ascii-banner',
		configureServer() {
			printBanner(packageJson, 'Powered by Moonkake')
		},
		closeBundle() {
			printBanner(packageJson, 'Powered by Moonkake')
		},
	}
}

